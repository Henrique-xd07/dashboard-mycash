// Função auxiliar para gerar datas nos últimos 3 meses
const getDateInMonthsAgo = (monthsAgo: number, day: number = 15): Date => {
  const date = new Date();
  date.setMonth(date.getMonth() - monthsAgo);
  date.setDate(day);
  return date;
};

